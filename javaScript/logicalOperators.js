// Logical Operators in JavaScript
let p = true;
let q = false;

console.log("AND Operator: ", p && q);
console.log("OR Operator: ", p || q);
console.log("NOT Operator: ", !p);
console.log("Double NOT Operator: ", !!q);

// Summary:
// - Logical operators in JavaScript include AND (&&), OR (||), and NOT (!).
// - The AND operator returns true if both operands are true, while the OR operator returns true if at least one operand is true.
// - The NOT operator inverts the boolean value of its operand.
// - Double NOT (!!) is often used to convert a value to its boolean equivalent.