// Non Primitive Data Types
let arr = [1, 2, 3, 4, 5]; // Array
let obj = { name: "Akash", age: 25 }; // Object
let func = function() {
    return "Hello, World!";
}; // Function

console.log("Array: ", arr);
console.log("Object: ", obj);
console.log("Function Output: ", func());

// Summary:
// - Non-primitive data types in JavaScript include Objects, Arrays, and Functions.
// - These data types are mutable and are compared by reference.
// - Modifying a non-primitive data type affects all references to that data.