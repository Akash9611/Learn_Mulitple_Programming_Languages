// String Interpolation is a way to embed variables in a string.
// This is comes under Template Literals in JavaScript.
// Template Literals are enclosed by backticks (` `) instead of single or double quotes.
// Variables and expressions can be embedded in a string using the ${} syntax.

let ceo = {
    name: "Akash Desai",
    age:27,
    company: "Google",
}

console.log(`${ceo.name} at the age of ${ceo.age} become the CEO of ${ceo.company} company`)