#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    char b = 'A';
    bool c = true;
    float d = 1.02f;
    double e = 1.123456;

    cout << "Integer: " << a << endl;
    cout << "Character: " << b << endl;
    cout << "Boolean: " << c << endl;
    cout << "Float: " << d << endl;
    cout << "Double: " << e << endl;

    return 0;
}